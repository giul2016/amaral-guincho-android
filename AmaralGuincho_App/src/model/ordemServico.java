package model;

public class ordemServico {
	
	int id_os, id_func, id_veiculo;
	String dtab_os, agendamento_os, status_os;
	
	public int getId_os() {
		return id_os;
	}
	public void setId_os(int id_os) {
		this.id_os = id_os;
	}
	public int getId_func() {
		return id_func;
	}
	public void setId_func(int id_func) {
		this.id_func = id_func;
	}
	public int getId_veiculo() {
		return id_veiculo;
	}
	public void setId_veiculo(int id_veiculo) {
		this.id_veiculo = id_veiculo;
	}
	public String getDtab_os() {
		return dtab_os;
	}
	public void setDtab_os(String dtab_os) {
		this.dtab_os = dtab_os;
	}
	public String getAgendamento_os() {
		return agendamento_os;
	}
	public void setAgendamento_os(String agendamento_os) {
		this.agendamento_os = agendamento_os;
	}
	public String getStatus_os() {
		return status_os;
	}
	public void setStatus_os(String status_os) {
		this.status_os = status_os;
	}
	
	

}
