package model;

public class servico {
	
	int id_servico;
	String tipo_servico;
	double precoR_servico, precoC_servico;
	
	
	public int getId_servico() {
		return id_servico;
	}
	public void setId_servico(int id_servico) {
		this.id_servico = id_servico;
	}
	public String getTipo_servico() {
		return tipo_servico;
	}
	public void setTipo_servico(String tipo_servico) {
		this.tipo_servico = tipo_servico;
	}
	public double getPrecoR_servico() {
		return precoR_servico;
	}
	public void setPrecoR_servico(double precoR_servico) {
		this.precoR_servico = precoR_servico;
	}
	public double getPrecoC_servico() {
		return precoC_servico;
	}
	public void setPrecoC_servico(double precoC_servico) {
		this.precoC_servico = precoC_servico;
	}
	
	
	

}
